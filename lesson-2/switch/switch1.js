let dayOfWeek = +prompt(`number of th day: 1-7`)
switch (dayOfWeek) {
   case (1): alert( "monday")
   break;
   case (2): alert( "tuesday")
   break;
   case (3): alert( "wednesday")
   break;
   case (4): alert( "thursday")
   break;
   case (5): alert( "friday")
   break;
   case (6): alert( "saturday")
   break;
   case (7): alert( "sunday")
   default: alert( "sunday")
}